package Beans;

public class memberBean {
	
	private String id;
	private String pwd;
	private String email;
	private String hp;
	private String scial_num;
	private String name;
	private String isMan;
	private String address;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getHp() {
		return hp;
	}
	public void setHp(String string) {
		this.hp = string;
	}
	public String getScial_num() {
		return scial_num;
	}
	public void setScial_num(String scial_num) {
		this.scial_num=scial_num;
	}
	public String getIsMan() {
		return isMan;
	}
	public void setIsMan(String isMan) {
		this.isMan = isMan;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
}

